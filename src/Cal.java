public class Cal {
    public static void main(String[] args) {
        int a , b, sum , mm , gg , na;

        a = 12;
        b = 2;

        sum = a+b;
        mm = a-b;
        gg = a*b;
        na = a/b;

        System.out.println("a+b = "+sum);
        System.out.println("a-b = "+mm);
        System.out.println("a*b = "+gg);
        System.out.println("a/b = "+na);
    }

}
